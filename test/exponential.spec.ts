import { assert } from "chai";
import { ethers } from "hardhat";
import { ExponentTest } from "../typechain-types";

const oneX96: bigint = BigInt(2) ** BigInt(96);

function assertPercision(actual: bigint, expected: bigint) {
    let error = expected - actual;
    error = error > BigInt(0) ? error : -error;
    const max_error = expected / BigInt(2) ** BigInt(32) + BigInt(1);
    assert(error < max_error, "error larger than expected");
}

describe("Exponent hash", function () {
    let exponentContract: ExponentTest;
    before(async () => {
        let exponentABI = await ethers.getContractFactory("ExponentTest");
        exponentContract = await exponentABI.deploy();
    });

    it("zero test", async () => {
        assert((await exponentContract.powTwo(0)).toBigInt() == oneX96);
        assert((await exponentContract.powHalf(0)).toBigInt() == oneX96);
    });

    it("one set-bit percision test", async () => {
        for (let i = 0; i < 64; i++) {
            const input = BigInt(2) ** BigInt(i);
            const answer: bigint = (await exponentContract.powTwo(input)).toBigInt();
            assertPercision(answer, answerSet1[i]);
            assertPercision(answer - oneX96, answerSet1[i] - oneX96);
        }

        for (let i = 0; i < 64; i++) {
            const input = BigInt(2) ** BigInt(i);
            const answer: bigint = (await exponentContract.powHalf(input)).toBigInt();
            assertPercision(answer, answerSet2[i]);
            assertPercision(oneX96 - answer, oneX96 - answerSet2[i]);
        }
    });

    it("one unset-bit percision test", async () => {
        for (let i = 0; i < 64; i++) {
            const input = BigInt(2) ** BigInt(64) - BigInt(2) ** BigInt(i);
            const answer: bigint = (await exponentContract.powTwo(input)).toBigInt();
            assertPercision(answer, answerSet3[i]);
            assertPercision(answer - oneX96, answerSet3[i] - oneX96);
        }

        for (let i = 0; i < 64; i++) {
            const input = BigInt(2) ** BigInt(64) - BigInt(2) ** BigInt(i);
            const answer: bigint = (await exponentContract.powHalf(input)).toBigInt();
            assertPercision(answer, answerSet4[i]);
            assertPercision(oneX96 - answer, oneX96 - answerSet4[i]);
        }
    });

    it("exponent integer part test", async () => {
        for (let i = 1; i < 160; i++) {
            const input = BigInt(i) * BigInt(2) ** BigInt(64);
            const answer: bigint = (await exponentContract.powTwo(input)).toBigInt();
            const expected: bigint = BigInt(2) ** BigInt(96 + i);
            assert(answer == expected);
        }

        for (let i = 1; i < 96; i++) {
            const input = BigInt(i) * BigInt(2) ** BigInt(64);
            const answer: bigint = (await exponentContract.powHalf(input)).toBigInt();
            const expected: bigint = BigInt(2) ** BigInt(96 - i);
            assert(answer == expected);
        }
    });

    it("super large input test", async () => {
        const input = BigInt(160) * BigInt(2) ** BigInt(64) - BigInt(1);
        const answer: bigint = (await exponentContract.powTwo(input)).toBigInt();
        const excepted = BigInt("115792089237316195419220029638716690198874166419800961959919449323396466059682");
        assertPercision(answer, excepted);

        const input2 = BigInt(160) * BigInt(2) ** BigInt(64);
        const answer2: bigint = (await exponentContract.powTwo(input2)).toBigInt();
        assert(answer2 == BigInt(2) ** BigInt(256) - BigInt(1));

        const input3 = BigInt(2) ** BigInt(255);
        const answer3: bigint = (await exponentContract.powTwo(input3)).toBigInt();
        assert(answer3 == BigInt(2) ** BigInt(256) - BigInt(1));
    });

    it("super small input test", async () => {
        for (let i = 93 * 8; i <= 97 * 8; i++) {
            const input = BigInt(i) * BigInt(2) ** BigInt(61);
            const answer: bigint = (await exponentContract.powHalf(input)).toBigInt();
            const expected = BigInt(Math.floor(2 ** (96 - i / 8)));
            assert(answer == expected);
        }

        const input2 = BigInt(2) ** BigInt(255);
        const answer2: bigint = (await exponentContract.powHalf(input2)).toBigInt();
        assert(answer2 == BigInt(0));
    });

    it("random tasks", async () => {
        for (let i = 0; i < randomTasks1.length; i++) {
            const [input, expected] = randomTasks1[i];
            const answer: bigint = (await exponentContract.powTwo(input)).toBigInt();
            assertPercision(answer, expected);
            assertPercision(answer - oneX96, expected - oneX96);
        }

        for (let i = 0; i < randomTasks2.length; i++) {
            const [input, expected] = randomTasks2[i];
            const answer: bigint = (await exponentContract.powHalf(input)).toBigInt();
            exponentContract.powHalf(input);
            assertPercision(answer, expected);
            assertPercision(oneX96 - answer, oneX96 - expected);
        }
    });
});

